 public static int asciiToCustom(char ascii)     // a mhetod which converts  an array of characters to custom code.( i generated my own custom code 0-94)
{
    int asci=ascii;                            // convert aschii char to its equvalent integer value
  int customCode;
  if(asci >=32 && asci <=126)                 //printable ascii characters which are b/n 32 && 126 (i am gust trying to map (32-126) to (1-94) )
  {
      customCode=asci - 31;          // coverting to custom code ,let 32=1  or 32-x=31 solving for x , x=1
  }
 else if (asci==10)                  // ascii 10 is new line "\n" which is difficult to print ,so i used another else if statement
{
     customCode=asci-10;             // here mapping 10 to 0 , or let 10=0   
 }
  else
  {
       throw new RuntimeException("Insert a valid letter");   // if it is out of bound (0-94) it will be run time Exeption 
  }
    return customCode;
    
}
    public static char customToAscii(int custom){          // method which converts my custom code to ascii code
    int ascii;
        if(custom >=1 && custom <=94){                    // if it is b/n 1 & 94 it just add 31 to reconvert it to its previous place : since 32=1
        ascii= custom +  31;
        }
        else if(custom==0){
        ascii= custom + 10;                             // 0=10
        }
        else{ throw new RuntimeException("not a valid custom code");}
        
      return (char) ascii;      
    }
    public static String encrypt(String plainText, int k){       //  this method takes plaintext and key then perform encryption
    char[] arr=plainText.toCharArray();
    for(int i = 0; i<arr.length;i++){
       
        arr[i]=customToAscii((asciiToCustom(arr[i]) + k ) % 95); //convert char to customCode then add key then perform module then again convert back to ascii code
       
     }
    return String.valueOf(arr);                              //  ascii value(int type) to string 
    }
    public static String decrypt(String cText, int k){    // it is decription method ,takes key an cipher text
         char[] arr=cText.toCharArray();
    for(int i = 0; i<arr.length;i++){
       
        arr[i]=customToAscii((asciiToCustom(arr[i]) + 95 - k % 95) %95);  // i add 95 for the case (ci-key) is negative and i have to module it two times for posetive value since i added 95 
      
   
    }
     return String.valueOf(arr);
    }
    
    
    
    
    
    
