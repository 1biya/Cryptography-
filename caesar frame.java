    private void fsaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_fsaveActionPerformed
        try{
            JFileChooser fileSaver= new JFileChooser();
            fileSaver.showSaveDialog(null);
            File file=fileSaver.getSelectedFile();
            if(!Files.exists(file.toPath())){
                pathField.setText(file.toPath().toString());
                FileWriter write=new FileWriter(file.toPath().toString());
                textArea.write(write);
                textArea.requestFocus();
                
            
            }
            else{
                JOptionPane.showMessageDialog(this,"File name alredy existed \n Change File Name ");
            }
            
        }catch(Exception e){JOptionPane.showMessageDialog(this,"Save Discarded");}
    }//GEN-LAST:event_fsaveActionPerformed

    private void browseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_browseActionPerformed
        JFileChooser chooser = new JFileChooser();                     
       String path;
         int result = chooser.showSaveDialog(null);               // display  save dialog
         
  if(result==JFileChooser.APPROVE_OPTION){                       // if save button is clicked
            File selectedFile=chooser.getSelectedFile();         // get selected file
   path = selectedFile.getAbsolutePath();                         // gets its path
             
            try {
                FileReader reade = new FileReader(path);              //  "FileReader" is a class ,reades the path and get the file ,it also reads the file
                //BufferedReader br = new BufferedReader(reade);        // reads the file line by line
                //br.close();
                textArea.read(reade,null);                    // reads from text area
                textArea.requestFocus();                 // to make the focus on the text area
            } catch (FileNotFoundException ex) {
                JOptionPane.showMessageDialog(this, "file not found");    //Exception if file not found
            } catch (IOException ex) {
                Logger.getLogger(frame.class.getName()).log(Level.SEVERE, null, ex);
            }catch(Exception e){
                
            }
            pathField.setText(path);                // displaying the path on the text field
  }
          
       
             
         
        
        
    }//GEN-LAST:event_browseActionPerformed

